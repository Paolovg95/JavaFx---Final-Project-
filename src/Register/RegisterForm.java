    private void registerjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerjButtonActionPerformed
        DBConnection con = new DBConnection();
        Connection reg = con.getConnection();

        String firstN = FirstNamejTextField.getText();
        String lastN = LastNamejTextField.getText();
        String userN = RUsernamejTextField.getText();
        String pass = String.valueOf(RPasswordField.getPassword());
        String confpass =  String.valueOf(RConfPasswordField1.getPassword());
        String email = EmailjTextField.getText();
        String cellphone = CellphonejTextField.getText();

        if (firstN.equals(" ")){
            JOptionPane.showMessageDialog(null, "Enter First Name");
        }
        else if (lastN.equals(" ")){
            JOptionPane.showMessageDialog(null, "Enter Last Name");
        }
        else if (pass.equals(" ") || confpass.equals(" ")) {
            JOptionPane.showMessageDialog(null, "Enter Password");
        }
        else if (!pass.equals(confpass)){
            JOptionPane.showMessageDialog(null, "Please Re-Enter Password");
            RPasswordField.setText(" ");
            RConfPasswordField1.setText(" ");
        }
        else if (CheckUsername(userN) == true ){
            JOptionPane.showMessageDialog(null, "Username Alredy exists");
            RUsernamejTextField.setText(" ");
        }
        else {
            PreparedStatement ps;
            String query = "insert into  Registration values (?,?,?,?,?,?) ";
        
                try {
                    ps = reg.prepareStatement(query);
                    ps.setString(1,firstN);
                    ps.setString(2,lastN);
                    ps.setString(3,userN);
                    ps.setString(4,pass);
                    ps.setString(5,email);
                    ps.setString(6,cellphone);
                    
                    
                    if  (ps.executeUpdate()>0){
                        JOptionPane.showMessageDialog(null," New User Added ! ");
                    }
                    FirstNamejTextField.setText("");
                    LastNamejTextField.setText("");
                    RUsernamejTextField.setText("");
                    RPasswordField.setText("");
                    RConfPasswordField1.setText("");
                    EmailjTextField.setText("");
                    CellphonejTextField.setText("");

                } catch (SQLException ex) {
                    Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            
        }
    }//GEN-LAST:event_registerjButtonActionPerformed

