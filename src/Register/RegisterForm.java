        public boolean CheckUsername(String username) {
            
            DBConnection con = new DBConnection();
            Connection reg = con.getConnection();
            ResultSet rs;
            boolean checkUser = false;
            String query = "select*from Registration where username=?";
            
            try {
                
                PreparedStatement ps = reg.prepareStatement(query);
                
                ps.setString(1,username);
                rs = ps.executeQuery();
                
            
                if (rs.next()){ // means that if the next row is not null (means if it exist)
                    checkUser=true;
                }
            }catch (SQLException e){
                Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE,null,e);
                
            }
            return checkUser;
        }
    

